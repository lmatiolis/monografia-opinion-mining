    private void btnGerarArquivosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGerarArquivosActionPerformed

        barraProgArquivosTreinoTeste.setMaximum(TelaPrincipal.baseDeTextoController.baseDeTextoModel.getQtdTextos());
        barraProgArquivosTreinoTeste.setStringPainted(true);
        barraProgArquivosTreinoTeste.setVisible(true);

        //Impossibilita redefinir quantidade, ja que optou-se por gerar os arquivos
        btnRedefinirQtd.setEnabled(false);
        
        btnGerarArquivos.setEnabled(false);
        labelGerarArquivos.setEnabled(false);

        //Cria um SwingWorker para não travar a tela e preencher a barra de progresso
        new SwingWorker() {

            @Override
            protected Object doInBackground() throws Exception {

                TelaPrincipal.baseDeTextoController.selecionaAmostraParaTreinoETeste(qtdSolicitadaTreino, qtdSolicitadaTeste, TelaPrincipal.baseDeTextoController.baseDeTextoModel);

                TelaPrincipal.btnProximo.setEnabled(true);

                if(qtdSolicitadaTreino == 0 && qtdSolicitadaTeste == 0){
                    TelaPrincipal.telaAtual = 11;
                }

                return null;

            }

        }.execute();

        
    }//GEN-LAST:event_btnGerarArquivosActionPerformed

