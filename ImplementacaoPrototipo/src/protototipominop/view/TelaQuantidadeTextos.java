    private void btnOkTreinoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkTreinoActionPerformed

        //Inicializa-se as variaveis
        int qtdRestanteTreino = 0;
        boolean digitouInteiro = false;

        //Verifica se o que foi digitado eh um numero inteiro
        try{

            qtdSolicitadaTreino = Integer.valueOf(tfQtdTreino.getText());
            digitouInteiro = true;

        } catch (Exception e){
            JOptionPane.showMessageDialog(null, "Por favor, digite um valor inteiro.", "Erro na operação!", JOptionPane.WARNING_MESSAGE);
        }

        //So realiza a exibicao na tela se o valor digitado for um numero inteiro
        if(digitouInteiro){

            if(qtdSolicitadaTreino < 0){

                JOptionPane.showMessageDialog(null, "Quantidade solicitada menor que 0. Valor inválido.", "Erro na operação!", JOptionPane.WARNING_MESSAGE);

            } else {

                //Verifica se quantidade solicitada nao excede a quantidade disponivel
                if (qtdTotalTextos - qtdSolicitadaTreino >=0 ){

                    labelRestaramTreino.setVisible(true);
                    labelRestaramTreino.setEnabled(true);

                    //Calcula quantidade restantes apos determinacao do numero
                    qtdRestanteTreino = qtdTotalTextos - qtdSolicitadaTreino;

                    //Mostra na tela a quantidade restante
                    labelQtdRestaramTreino.setText(Integer.toString(qtdRestanteTreino) + " documento(s)");

                    labelQtdRestaramTreino.setVisible(true);
                    labelQtdRestaramTreino.setEnabled(true);

                    btnConfirmaTreino.setVisible(true);
                    btnConfirmaTreino.setEnabled(true);

                } else {
                    JOptionPane.showMessageDialog(null, "Quantidade solicitada maior que a disponivel.", "Erro na operação!", JOptionPane.WARNING_MESSAGE);
                }

            }

        }

    }//GEN-LAST:event_btnOkTreinoActionPerformed

    private void btnOkTesteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkTesteActionPerformed

        //Inicializa as variaveis
        int qtdRestanteTeste = 0;
        boolean digitouInteiro = false;

        //Verifica se o valor digitado eh um valor inteiro
        try{

            qtdSolicitadaTeste = Integer.valueOf(tfQtdTeste.getText());
            digitouInteiro = true;

        } catch (Exception e){
            JOptionPane.showMessageDialog(null, "Por favor, digite um valor inteiro.", "Erro na operação!", JOptionPane.WARNING_MESSAGE);
        }

        //So realiza a exibicao na tela se o valor digitado for um numero inteiro
        if(digitouInteiro){

            if(qtdSolicitadaTeste < 0){

                JOptionPane.showMessageDialog(null, "Quantidade solicitada menor que 0. Valor inválido.", "Erro na operação!", JOptionPane.WARNING_MESSAGE);

            } else {

                //Calcula a quantidade restante para teste
                qtdRestanteTeste = (qtdTotalTextos - qtdSolicitadaTreino) - qtdSolicitadaTeste;

                if(qtdRestanteTeste >= 0){

                    labelRestaramTeste.setVisible(true);
                    labelRestaramTeste.setEnabled(true);

                    //Mostra a quantidade na tela
                    labelQtdRestaramTeste.setText(Integer.valueOf(qtdRestanteTeste) + " documento(s)");
                    labelQtdRestaramTeste.setVisible(true);
                    labelQtdRestaramTeste.setEnabled(true);

                    btnConfirmaTeste.setVisible(true);
                    btnConfirmaTeste.setEnabled(true);

                } else{
                    JOptionPane.showMessageDialog(null, "Quantidade solicitada maior que a disponivel.", "Erro na operação!", JOptionPane.WARNING_MESSAGE);
                }

            }


            
           
        }

    }//GEN-LAST:event_btnOkTesteActionPerformed

    private void btnConfirmaTreinoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmaTreinoActionPerformed

            //Impossibilita edicao dos valores de treino
            labelInfoQtdTreinamento.setEnabled(false);
            tfQtdTreino.setEnabled(false);
            btnOkTreino.setEnabled(false);
            labelRestaramTreino.setEnabled(false);
            labelQtdRestaramTreino.setEnabled(false);
            btnConfirmaTreino.setEnabled(false);

            if(qtdSolicitadaTreino != 0){

                //Possibilita edicao dos valores de teste
                labelInfoQtdTeste.setEnabled(true);
                tfQtdTeste.setEnabled(true);
                btnOkTeste.setEnabled(true);

                tfQtdTeste.requestFocus();

            } else {

                tfQtdTeste.setText("0");
                btnOkTeste.setEnabled(true);
                btnOkTeste.doClick();
                btnConfirmaTeste.doClick();
                
            }

            

    }//GEN-LAST:event_btnConfirmaTreinoActionPerformed

