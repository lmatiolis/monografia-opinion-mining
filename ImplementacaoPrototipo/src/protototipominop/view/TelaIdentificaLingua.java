    private void btnSimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimActionPerformed

        labelCriarArquivoSeparado.setEnabled(false);
        btnNao.setEnabled(false);
        btnSim.setEnabled(false);

        barraArquivoAtehMomento.setVisible(true);

        //Recupera a quantidade de docs que foram processados ja
        int docParou = Integer.valueOf(barraProgIdLingua.getString().split(" ")[0]);

        //Coloca o maximo como a quantidade restante de documentos
        barraArquivoAtehMomento.setMaximum(barraProgIdLingua.getMaximum() - docParou);
        barraArquivoAtehMomento.setStringPainted(true);

        //Cria um SwingWorker para não travar a tela e preencher a barra de progresso
        new SwingWorker() {

            @Override
            protected Object doInBackground() throws Exception {

                TelaPrincipal.baseDeTextoController.separaArquivoIdLinguas(caminhoArquivo);

                return null;
            }

        }.execute();

    }//GEN-LAST:event_btnSimActionPerformed


