    private void btnIniciarIdentificacaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarIdentificacaoActionPerformed

        btnIniciarIdentificacao.setEnabled(false);

        btnCancelar.setEnabled(true);

        //Cria um SwingWorker para não travar a tela e preencher a barra de progresso
        worker = new SwingWorker <Object, Object>() {

            @Override
            protected Object doInBackground() throws Exception {

                barraProgIdLingua.setVisible(true);
                barraProgIdLingua.setStringPainted(true);
                barraProgIdLingua.setMaximum(TelaPrincipal.baseDeTextoController.contaLinhas(caminhoArquivo));

                TelaPrincipal.baseDeTextoController.identificaLingua(caminhoArquivo);

                JOptionPane.showMessageDialog(null, "Identificação realizada com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);

                btnCancelar.setEnabled(false);

                return null;
            }
            
        };

        worker.execute();
        
    }//GEN-LAST:event_btnIniciarIdentificacaoActionPerformed

    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed

        //Cancela a thread
        worker.cancel(true);

        btnCancelar.setEnabled(false);

        labelCriarArquivoSeparado.setVisible(true);
        btnNao.setVisible(true);
        btnSim.setVisible(true);

}//GEN-LAST:event_btnCancelarActionPerformed

